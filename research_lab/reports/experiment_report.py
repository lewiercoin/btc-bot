from __future__ import annotations

from typing import Any

from research_lab.experiments.manifest import DataManifest


def generate_report(
    *,
    experiment_id: str,
    hypothesis: dict[str, Any],
    metrics: dict[str, Any],
    baseline_metrics: dict[str, Any],
    gate_results: list[dict[str, Any]],
    verdict: str,
    data_manifests: list[DataManifest],
    artifacts: dict[str, Any],
) -> str:
    lines: list[str] = []
    name = hypothesis.get("name", hypothesis.get("hypothesis_id", "Unknown Hypothesis"))
    lines.append(f"# {name} - Research Report")
    lines.append("")
    lines.append(f"**Experiment ID:** {experiment_id}")
    lines.append(f"**Hypothesis ID:** {hypothesis.get('hypothesis_id', '')}")
    lines.append(f"**Status:** {verdict}")
    lines.append("")
    lines.append("## Executive Summary")
    lines.append("")
    lines.append(f"- **Verdict:** {verdict}")
    lines.append(f"- **Key Finding:** {hypothesis.get('expected_observation', 'Not specified')}")
    lines.append("- **Recommendation:** Human/auditor review required before any next action")
    lines.append("")
    lines.append("## Hypothesis")
    lines.append("")
    lines.append(f"- **Class:** {hypothesis.get('class', '')}")
    lines.append(f"- **Edge Rationale:** {hypothesis.get('edge_rationale', '')}")
    lines.append(f"- **Counterparty:** {hypothesis.get('counterparty_or_market_mechanism', '')}")
    lines.append(f"- **Expected Observation:** {hypothesis.get('expected_observation', '')}")
    lines.append("")
    lines.append("## Data Sources / Manifests")
    lines.append("")
    lines.append("| Dataset ID | Timeframe | Symbol | Date Range | Row Count | Hash |")
    lines.append("|---|---|---|---|---:|---|")
    for manifest in data_manifests:
        lines.append(
            f"| {manifest.dataset_id} | {manifest.timeframe} | {manifest.symbol} | "
            f"{manifest.date_start} to {manifest.date_end} | {manifest.row_count} | "
            f"{manifest.compute_hash()[:12]} |"
        )
    lines.append("")
    lines.append("## Baseline Comparison")
    lines.append("")
    lines.append("| Metric | Baseline | Experiment | Delta |")
    lines.append("|---|---:|---:|---:|")
    for key in sorted(set(baseline_metrics) | set(metrics)):
        base = baseline_metrics.get(key)
        actual = metrics.get(key)
        delta = _delta(base, actual)
        lines.append(f"| {key} | {_fmt(base)} | {_fmt(actual)} | {_fmt(delta)} |")
    lines.append("")
    lines.append("## Metrics")
    lines.append("")
    lines.append("| Metric | Value |")
    lines.append("|---|---:|")
    for key, value in sorted(metrics.items()):
        lines.append(f"| {key} | {_fmt(value)} |")
    lines.append("")
    lines.append("## Gates")
    lines.append("")
    lines.append("| Gate | Threshold | Actual | Status | Severity |")
    lines.append("|---|---:|---:|---|---|")
    for result in gate_results:
        status = "PASS" if result.get("passed") else "FAIL"
        threshold = f"{result.get('operator', '')} {result.get('threshold', '')}"
        lines.append(
            f"| {result.get('name', '')} | {threshold} | "
            f"{_fmt(result.get('actual_value'))} | {status} | {result.get('severity', '')} |"
        )
    lines.append("")
    lines.append("## Verdict")
    lines.append("")
    lines.append(f"{verdict}")
    lines.append("")
    lines.append("## Limitations")
    lines.append("")
    lines.append("- This report is generated from provided metrics and gates only.")
    lines.append("- Backtest execution and statistical validation remain experiment-specific.")
    lines.append("")
    lines.append("## Artifacts")
    lines.append("")
    for key, value in sorted(artifacts.items()):
        lines.append(f"- **{key}:** `{value}`")
    lines.append("")
    lines.append("## Next-Step Recommendation")
    lines.append("")
    lines.append("Review the experiment with Claude Code before promoting or scheduling follow-up work.")
    lines.append("")
    lines.append("---")
    lines.append("*Generated by research_lab experiment framework*")
    return "\n".join(lines)


def _delta(base: Any, actual: Any) -> float | None:
    if isinstance(base, (int, float)) and isinstance(actual, (int, float)):
        return float(actual) - float(base)
    return None


def _fmt(value: Any) -> str:
    if value is None:
        return "-"
    if isinstance(value, float):
        return f"{value:.4f}"
    return str(value)
